public class incer_decre {
    public static void main(String[] args) {
        int a=10;

        System.out.println("Increment...");
        System.out.println(a++); //Print  10... value 11 assign
        System.out.println(++a); //12 value assign and print a=12

        System.out.println("Decrement...");
        System.out.println(a--); //print 12 value assign 11
        System.out.println(--a); // value assign 10   print 10

    }

}
